const mostraIdade = (nome, idade) => {
  return `A idade de ${nome} é ${idade}`;
}

const mostraCidade = (nome, cidade) => {
  return `A cidade de ${nome} é ${cidade}`;
}

const mostraHobby = (nome, hobby) => {
  return `O hobby de ${nome} é ${hobby}`;
}

export {
  mostraIdade,
  mostraCidade,
  mostraHobby
};
